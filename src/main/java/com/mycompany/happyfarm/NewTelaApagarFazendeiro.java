    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
      int codigo = Integer.parseInt(intcodigo.getText());
    
    // Cria uma conexão com o banco de dados
    ConnectionFactory connectionFactory = new ConnectionFactory();
    try (Connection conn = connectionFactory.obtemConexao()) {
        // Cria uma consulta SQL parametrizada para buscar o fazendeiro pelo código
        String sql = "SELECT * FROM tbusuario WHERE cod = ?";
        try (PreparedStatement stmt = conn.prepareStatement(sql)) {
            stmt.setInt(1, codigo);
            try (ResultSet rs = stmt.executeQuery()) {
                // Se houver resultados, preencha os campos com as informações do fazendeiro
                if (rs.next()) {
                    txtcnpj.setText(rs.getString("cnpj"));
                    txtEmail.setText(rs.getString("emailCorp"));
                    txtfone.setText(rs.getString("fone"));
                    txtendereco.setText(rs.getString("endereco"));
                    Intendereco.setText(rs.getString("numEnd"));
                    txtbairro.setText(rs.getString("bairro"));
                    txtcidade.setText(rs.getString("cidade"));
                    txtestado.setText(rs.getString("estado"));
                    txtcep.setText(rs.getString("cep"));
                    txtsenha.setText(rs.getString("senha"));
                    JOptionPane.showMessageDialog(null, "Fazendeiro encontrado e campos preenchidos.");
                } else {
                    JOptionPane.showMessageDialog(null, "Nenhum fazendeiro encontrado com o código especificado.");
                }
            }
        }
    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Erro ao consultar o fazendeiro: " + ex.getMessage());
    }
    }//GEN-LAST:event_jButton3ActionPerformed

