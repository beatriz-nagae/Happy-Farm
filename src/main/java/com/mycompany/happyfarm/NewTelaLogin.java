    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (loginAttempts >= 3) {
            JOptionPane.showMessageDialog(this, "Usu√°rio bloqueado ap√≥s 3 tentativas falhas.", "Login Bloqueado", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Fazendeiro f = new Fazendeiro();
        f.setCnpj(txtcnpj.getText());
        f.setSenha(txtsenha.getText());
        setDefaultCloseOperation(DISPOSE_ON_CLOSE);

        try {
            boolean loginSuccess = f.login(f.getCnpj(), f.getSenha());
            if (!loginSuccess) {
                loginAttempts++;
                JOptionPane.showMessageDialog(this, "CNPJ ou senha incorretos. Tentativas restantes: " + (3 - loginAttempts), "Erro de Login", JOptionPane.ERROR_MESSAGE);
                if (loginAttempts >= 3) {
                    jButton1.setEnabled(false);
                }
            } else {
                // Reset the attempt counter on successful login
                loginAttempts = 0;
                dispose();
                // Proceed with successful login actions
            }
        } catch (SQLException ex) {
            Logger.getLogger(NewTelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        }   // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

